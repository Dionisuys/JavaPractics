public class Solution {
    public static String removeStars(String s) {
        // Создаю StringBuilder с заданной строкой
        StringBuilder sb = new StringBuilder(s);
        // Инициализирую переменную i
        int i = 0;
        while (i < sb.length()) {
            // Если текущий символ - звездочка
            if (sb.charAt(i) == '*') {
                // Ищу ближайший не-звездочный символ слева
                int j = i - 1;
                while (j >= 0 && sb.charAt(j) == '*') {
                    j--;
                }
                if (j >= 0) {
                    // Удаляю ближайший не-звездочный символ слева и текущую звездочку
                    sb.deleteCharAt(j);
                    sb.deleteCharAt(i - 1);
                    // Обновляю значение i, чтобы начать проверку со следующей звездочки
                    i = j;
                } else {
                    // Удаляю текущую звездочку, если нет не-звездочного символа слева
                    sb.deleteCharAt(i);
                    /*Если следующий символ также звездочка, удаляю следующий
                    символ (левый символ уже удален)*/
                    if (sb.charAt(i) == '*') {
                        sb.deleteCharAt(i - 1);
                    } else {
                        /*Иначе удаляю левый символ и обновляю значение i,
                        чтобы начать проверку со следующей звездочки*/
                        sb.deleteCharAt(i - 1);
                        i--;
                    }
                }
            } else {
                // Если текущий символ не звездочка, продолжаю проверку следующего символа
                i++;
            }
        }
        // Возвращаю измененную строку
        return sb.toString();
    }
}
